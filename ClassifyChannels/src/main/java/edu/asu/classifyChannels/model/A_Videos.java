package edu.asu.classifyChannels.model;

public class A_Videos {

	private String id;
	private String snippet;
	private String contentDetails;
	private String fileDetails;
	private String processingDetails;
	private String recordingDetails;
	private String statistics;
	private String status;
	private String suggestions;
	private String topicDetails;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getSnippet() {
		return snippet;
	}
	public void setSnippet(String snippet) {
		this.snippet = snippet;
	}
	public String getContentDetails() {
		return contentDetails;
	}
	public void setContentDetails(String contentDetails) {
		this.contentDetails = contentDetails;
	}
	public String getFileDetails() {
		return fileDetails;
	}
	public void setFileDetails(String fileDetails) {
		this.fileDetails = fileDetails;
	}
	public String getProcessingDetails() {
		return processingDetails;
	}
	public void setProcessingDetails(String processingDetails) {
		this.processingDetails = processingDetails;
	}
	public String getRecordingDetails() {
		return recordingDetails;
	}
	public void setRecordingDetails(String recordingDetails) {
		this.recordingDetails = recordingDetails;
	}
	public String getStatistics() {
		return statistics;
	}
	public void setStatistics(String statistics) {
		this.statistics = statistics;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getSuggestions() {
		return suggestions;
	}
	public void setSuggestions(String suggestions) {
		this.suggestions = suggestions;
	}
	public String getTopicDetails() {
		return topicDetails;
	}
	public void setTopicDetails(String topicDetails) {
		this.topicDetails = topicDetails;
	}
}
